            <h4 class="section-heading">  Fala constantemente em público no seu trabalho? Tem dificuldades em se expressar nas apresentações? </h4>

            <p>Você sabia que vários executivos estão optando pelo teatro?</p>

            <p>Sim, o teatro; Por desenvolver técnicas para solucionar o medo e a vergonha de falar no ambiente corporativo. É justamente esse assunto que iremos abordar neste artigo. Portanto, se lhe pareceu interessante até aqui, vem com a gente e descubra novas possibilidades.</p>
            
            <p>ARTE NO CORPORATIVO </p>
            
            <p>Falar em público, apresentar um projeto, são ofícios presentes no dia-a-dia de um executivo.</p> 

            <p>Para muitos essas tarefas são vistas como um desafio, a timidez e o medo do julgamento alheio levam os profissionais a não expor o máximo de si, escondendo sua real personalidade e seus conhecimentos. </p>

            <p>Ao se apresentar em público no trabalho, seja para transmitir uma mensagem, apresentar um projeto ou os seus resultados; Sua voz fica trêmula, dá aquele frio na barriga, falta de ar, fica andando de um lado para o outro, não sabe onde colocar as mãos, se perde nas palavras e quando precisa improvisar acaba se perdendo todo e travando? </p>

            <p>Todos esses fatores citados anteriormente podem ser trabalhados com técnicas teatrais. Se comunicar bem no ambiente corporativo hoje é fundamental para a evolução do profissional e seu crescimento dentro da empresa. </p>

            <p>As técnicas teatrais surgem como ferramentas para potencializar o profissional, fazendo com que ele esteja presente em sua apresentação de forma viva e segura.</p>

            <p>Elas que são usadas, normalmente, para fins artísticos, acabam então se tornando uma maneira de preparar o executivo para se comunicar bem, através de um autoconhecimento dos seus sentidos, gestos e ações. Podendo assim estar melhor preparado para enfrentar os desafios impostos no dia-a-dia corporativo.</p>



            <h4>1 - VOZ / RESPIRAÇÃO</h4>
            <a href="#">
              <img class="img-fluid" src="https://imageshack.com/i/pmBPJXXHp" alt="">
            </a>

            <p>Os exercícios de voz trabalham a maneira de pronunciar palavras, a projeção da fala em um volume e entonação ideal para cada momento, além da articulação verbal, tornando-a mais clara, precisa e objetiva.
            Exercícios de respiração surgem como um complemento para a voz, uma boa respiração ajuda o profissional a falar com mais clareza, expor o real sentido e objetivo da frase dita. </p>

            <h4>2- CORPO</h4>

            <a href="#">
              <img class="img-fluid" src="https://imageshack.com/i/pogFgXPEp" alt="">
            </a>

            <p>Os exercícios corporais ajudam a ter uma melhor consciência, trazendo  movimentos mais objetivos, para transparecer segurança. Com o controle sobre os gestos, sabendo quais deve fazer e quais deve evitar para que a mensagem seja a mais clara possível.</p>

            <h4>3- EMOCIONAL</h4>

            <a href="#">
              <img class="img-fluid" src="https://imageshack.com/i/pmCV5bG8p" alt="">
            </a>

            <p>Exercícios para o emocional ajudam o profissional a controlar sua ansiedade, nervosismo e a vergonha. Passando mais segurança e tranquilidade na hora da fala, fazendo com que a mensagem seja melhor transmitida e captada.</p>

            <h4>4- IMPROVISO </h4>

            <a href="#">
              <img class="img-fluid" src="https://imageshack.com/i/pmCAAH2bp" alt="">
            </a>

            <p>A improvisação é uma ferramenta importantíssima para o ator, pois ela desenvolve a capacidade de resolver o inesperado. Dentro das empresas, as técnicas que os executivos aprendem com o teatro, podem ajudar a resolver de forma criativa e espontânea determinados imprevistos em uma apresentação, ou em tarefas na empresa. Essa técnica fará com que você desenvolva o famoso "jogo de cintura", tão almejado em ambientes corporativos.</p>

            <h4>5- AUTOCONHECIMENTO</h4>

            <a href="#">
              <img class="img-fluid" src="https://imageshack.com/i/poLCiktsp" alt="">
            </a>

            <p>Ao colocar em prática todos os ensinamentos citados anteriormente, o profissional se torna um melhor conhecedor de si próprio, tanto do seu corpo quanto do seu inconsciente. Uma pessoa que se conhece melhor, consequentemente é um profissional ciente da suas capacidades, ações e mais seguro para agir em momentos de imprevisibilidade.</p>

